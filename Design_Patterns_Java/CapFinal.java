// Frameworks
// Um framework é como um software incompleto, o qual possui pontos com funcionalidades fixas
// (frozen spots) e pontos com funcionalidades variáveis (hotspots) que são pontos de extensão,
// o código da aplicação. Já os frozen spots são responsáveis pelas funcionalidades do framework
// e coordenação das execuções dos hotspots.
// O framework pode abstrair muitos dos seus hotspots, tendo uma implementação default, assim,
// o desenvolvedor não precisa conhecer e configurar tudo, tendo uma curva de aprendizado suave.


